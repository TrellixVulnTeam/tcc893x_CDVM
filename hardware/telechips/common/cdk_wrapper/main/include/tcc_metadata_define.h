#define GetUnalignedWord( pb, w ) \
            (w) = ((uint16_t) *(pb + 1) << 8) + *pb;

#define GetUnalignedDword( pb, dw ) \
            (dw) = ((uint32_t) *(pb + 3) << 24) + \
                   ((uint32_t) *(pb + 2) << 16) + \
                   ((uint16_t) *(pb + 1) << 8) + *pb;

#define GetUnalignedQword( pb, qw ) \
            (qw) = ((uint64_t) *(pb + 7) << 56) + \
                   ((uint64_t) *(pb + 6) << 48) + \
                   ((uint64_t) *(pb + 5) << 40) + \
                   ((uint64_t) *(pb + 4) << 32) + \
                   ((uint32_t) *(pb + 3) << 24) + \
                   ((uint32_t) *(pb + 2) << 16) + \
                   ((uint16_t) *(pb + 1) << 8) + *pb;

#define GetUnalignedWordEx( pb, w )     GetUnalignedWord( pb, w ); (pb) += sizeof(uint16_t);
#define GetUnalignedDwordEx( pb, dw )   GetUnalignedDword( pb, dw ); (pb) += sizeof(uint32_t);
#define GetUnalignedQwordEx( pb, qw )   GetUnalignedQword( pb, qw ); (pb) += sizeof(uint64_t);

#define LoadBYTE( b, p )    b = *(uint8_t *)p;  p += sizeof( uint8_t )
#define LoadWORD( w, p )    GetUnalignedWordEx( p, w )
#define LoadDWORD( dw, p )  GetUnalignedDwordEx( p, dw )
#define LoadQWORD( qw, p )  GetUnalignedQwordEx( p, qw )

